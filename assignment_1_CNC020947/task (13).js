var a = 10;
document.write("Result:"+
	"<br />The value of a is: "+a+
	"<br />......................................")
++a;
document.write("<br /><br />The value of ++a is: "+ a +
	"<br />Now the value of a is: "+ a);
a++;
document.write("<br /><br />The value of a++ is: "+ a +
	"<br />Now the value of a is: "+ a);

--a;
document.write("<br /><br />The value of --a is: "+ a +
	"<br />Now the value of a is: "+ a);

a--;
document.write("<br /><br />The value of a-- is: "+ a +
	"<br />Now the value of a is: "+ a);



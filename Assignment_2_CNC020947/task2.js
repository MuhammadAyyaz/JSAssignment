var traffic_signal = prompt("Enter the color traffic_signal from (red,green,yellow): ")
if (traffic_signal == "red") {
	alert("Must Stop")
}
else if (traffic_signal == "yellow") {
	alert("Ready to Move")
}
else if (traffic_signal == "green") {
	alert("Move Now")
}
else {
	alert("drive carefully")
}	

